    {
        $validatedData = $request->validate([
            'body' => 'required'
        ]);


        $validatedData['user_id'] = auth()->user()->id;
        $validatedData['news_id'] = $request['news_id'];

        Comment::create($validatedData);

        return back()->with('message', 'Success Add Comments !');
    }

